# Ex.01: Crie um programa que calcula a área de um quadrado. Leia o valor do lado.
print("--- Programa - Área do Quadrado ---")
# Entrada de Dados:
lado = float(input("Digitar o tamanho do lado: "))

# Processamento
areaQuadrado = lado * lado # Opção 2: lado ** 2

# Saída de Dados:
print("A área do quadrado é:", areaQuadrado)